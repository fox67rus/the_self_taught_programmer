# срез строки
ivan = "Петр Иванович успокоился и с интересом стал расспрашивать подробности о кончине Ивана Ильича."

print(ivan[:24]) # если начальный индекс 0, то оставить пустым
print(ivan[24:]) # если конечный индекс - последний символ, то оставить пустым
print(ivan[:]) # вернет исходный текст
print(ivan[::-1]) # строка наоборот




